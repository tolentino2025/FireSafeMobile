export async function generateHydrostaticTestPdf(options: HydrostaticPdfOptions): Promise<string> {
  const html = await generateHtml(options);
  const { uri } = await Print.printToFileAsync({ html });
  return uri;
}

export async function generateAndPrintHydrostaticTestPdf(options: HydrostaticPdfOptions): Promise<void> {
  const html = await generateHtml(options);
  await Print.printAsync({ html });
}

export async function generateAndShareHydrostaticTestPdf(options: HydrostaticPdfOptions): Promise<void> {
  const uri = await generateHydrostaticTestPdf(options);
  if (await Sharing.isAvailableAsync()) {
    await Sharing.shareAsync(uri, {
      mimeType: "application/pdf",
      dialogTitle: options.language === "pt-BR" ? "Compartilhar Relatorio" : "Share Report",
      UTI: "com.adobe.pdf",
    });
  }
}

export async function generateAndEmailHydrostaticTestPdf(options: HydrostaticPdfOptions & { recipientEmail?: string }): Promise<void> {
  const uri = await generateHydrostaticTestPdf(options);
  const { hydrostaticTest, language } = options;
  
  const subject = language === "pt-BR" 
    ? `Relatorio Teste Hidrostatico - ${hydrostaticTest.owner.corporateName} - ${hydrostaticTest.testDate}`
    : `Hydrostatic Test Report - ${hydrostaticTest.owner.corporateName} - ${hydrostaticTest.testDate}`;
  
  const body = language === "pt-BR"
    ? `Segue em anexo o relatorio do teste hidrostatico realizado em ${hydrostaticTest.testDate}.\n\nAtenciosamente,\nFireSafe ITM`
    : `Please find attached the hydrostatic test report conducted on ${hydrostaticTest.testDate}.\n\nBest regards,\nFireSafe ITM`;

  await MailComposer.composeAsync({
    recipients: options.recipientEmail ? [options.recipientEmail] : [],
    subject,
    body,
    attachments: [uri],
  });
}
