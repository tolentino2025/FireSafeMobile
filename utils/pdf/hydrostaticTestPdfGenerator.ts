    if (ref === "NFPA_13") return "NFPA 13";
    if (ref === "NFPA_14") return "NFPA 14";
    if (ref === "NFPA_25") return "NFPA 25";
    if (ref === "FM_GLOBAL") return "FM Global" + (h.fmDataSheet ? ` (${h.fmDataSheet})` : "");
